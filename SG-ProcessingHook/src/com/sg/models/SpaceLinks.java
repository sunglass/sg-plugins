package com.sg.models;

public class SpaceLinks {

private String project;
private String parentSpace;
private String subSpaces;
private String metamodels;
private String views;

public String getProject() {
return project;
}

public void setProject(String project) {
this.project = project;
}

public String getParentSpace() {
return parentSpace;
}

public void setParentSpace(String parentSpace) {
this.parentSpace = parentSpace;
}

public String getSubSpaces() {
return subSpaces;
}

public void setSubSpaces(String subSpaces) {
this.subSpaces = subSpaces;
}

public String getMetamodels() {
return metamodels;
}

public void setMetamodels(String metamodels) {
this.metamodels = metamodels;
}

public String getViews() {
return views;
}

public void setViews(String views) {
this.views = views;
}
	
}
