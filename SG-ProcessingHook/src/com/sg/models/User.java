package com.sg.models;

public class User {
	private User user;
	private String role;
	private String space;

	public User getUser() {
	return user;
	}

	public void setUser(User user) {
	this.user = user;
	}

	public String getRole() {
	return role;
	}

	public void setRole(String role) {
	this.role = role;
	}

	public String getSpace() {
	return space;
	}

	public void setSpace(String space) {
	this.space = space;
	}


}
