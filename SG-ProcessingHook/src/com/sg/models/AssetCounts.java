package com.sg.models;

public class AssetCounts {

private Integer spaces;
private Integer collaborators;
private Integer models;
private Integer metaModels;
private Integer notes;


public Integer getSpaces() {
return spaces;
}

public void setSpaces(Integer spaces) {
this.spaces = spaces;
}

public Integer getCollaborators() {
return collaborators;
}

public void setCollaborators(Integer collaborators) {
this.collaborators = collaborators;
}

public Integer getModels() {
return models;
}

public void setModels(Integer models) {
this.models = models;
}

public Integer getMetaModels() {
return metaModels;
}

public void setMetaModels(Integer metaModels) {
this.metaModels = metaModels;
}

public Integer getNotes() {
return notes;
}

public void setNotes(Integer notes) {
this.notes = notes;
}

}